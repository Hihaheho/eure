#[allow(dead_code)]
#[derive(Debug, Clone, Copy, PartialEq, Eq, PartialOrd, Ord, Hash)]
pub enum TerminalKind {
    NewLine,
    Whitespace,
    LineComment,
    BlockComment,
    Float,
    Integer,
    True,
    False,
    Null,
    Hole,
    Str,
    Text,
    CodeBlock,
    NamedCode,
    Code,
    GrammarNewline,
    Ws,
    At,
    DollarDollar,
    Dollar,
    Dot,
    LBrace,
    RBrace,
    LBracket,
    RBracket,
    LParen,
    RParen,
    Bind,
    Comma,
    Esc,
    TextStart,
    Ident,
}
#[allow(dead_code)]
impl TerminalKind {
    pub fn from_terminal_index(index: u16) -> Self {
        match index {
            1 => Self::NewLine,
            2 => Self::Whitespace,
            3 => Self::LineComment,
            4 => Self::BlockComment,
            5 => Self::Float,
            6 => Self::Integer,
            7 => Self::True,
            8 => Self::False,
            9 => Self::Null,
            10 => Self::Hole,
            11 => Self::Str,
            12 => Self::Text,
            13 => Self::CodeBlock,
            14 => Self::NamedCode,
            15 => Self::Code,
            16 => Self::GrammarNewline,
            17 => Self::Ws,
            18 => Self::At,
            19 => Self::DollarDollar,
            20 => Self::Dollar,
            21 => Self::Dot,
            22 => Self::LBrace,
            23 => Self::RBrace,
            24 => Self::LBracket,
            25 => Self::RBracket,
            26 => Self::LParen,
            27 => Self::RParen,
            28 => Self::Bind,
            29 => Self::Comma,
            30 => Self::Esc,
            31 => Self::TextStart,
            32 => Self::Ident,
            _ => panic!("Invalid terminal index: {}", index),
        }
    }
    pub fn is_builtin_terminal(&self) -> bool {
        matches!(
            self,
            TerminalKind::NewLine
                | TerminalKind::Whitespace
                | TerminalKind::LineComment
                | TerminalKind::BlockComment
        )
    }
    pub fn is_builtin_new_line(&self) -> bool {
        matches!(self, TerminalKind::NewLine)
    }
    pub fn is_builtin_whitespace(&self) -> bool {
        matches!(self, TerminalKind::Whitespace)
    }
    pub fn is_builtin_line_comment(&self) -> bool {
        matches!(self, TerminalKind::LineComment)
    }
    pub fn is_builtin_block_comment(&self) -> bool {
        matches!(self, TerminalKind::BlockComment)
    }
}

